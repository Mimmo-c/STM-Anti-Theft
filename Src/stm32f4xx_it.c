/**
  * @brief This function handles TIM1 update interrupt and TIM10 global interrupt.
  */
void TIM1_UP_TIM10_IRQHandler(void)
{
  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 0 */

  HAL_TIM_Base_Stop_IT(&htim2);         //Stop TIM2 in interrupt mode
  HAL_TIM_Base_Stop_IT(&htim1);         //Stop TIM1 in interrupt mode
  HAL_TIM_Base_Stop_IT(&htim3);         //Stop TIM3 in interrupt mode
  
  //Reset PA5 pin state
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET);         //LED OFF
  
  //Reset PA6 pin state
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6, GPIO_PIN_RESET);         //Sound OFF
  
  /* USER CODE END TIM1_UP_TIM10_IRQn 0 */
  HAL_TIM_IRQHandler(&htim1);
  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 1 */

  /* USER CODE END TIM1_UP_TIM10_IRQn 1 */
}

